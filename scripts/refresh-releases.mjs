// Regenerates releases.json from each component repo's latest release.
//
// Run in CI (see .github/workflows/refresh-releases.yml) with GITHUB_TOKEN.
// All reads are either public repos or this repo's own releases (the mirrored
// mobile APK), so the workflow's default GITHUB_TOKEN is sufficient — no
// cross-repo PAT needed here. (The mobile→get APK *upload* is what needs a
// token, and that lives in the mobile repo's workflow.)

const OWNER = "exfer-stack";
const SELF = `${OWNER}/get`;
const token = process.env.GITHUB_TOKEN || "";

const PUBLIC = {
  desktop: { repo: `${OWNER}/exfer-walletd-desktop`, kind: "desktop" },
  node: { repo: `${OWNER}/exfer`, kind: "source" },
  indexer: { repo: `${OWNER}/exfer-indexer`, kind: "source" },
  walletd: { repo: `${OWNER}/exfer-walletd`, kind: "source" },
  py: { repo: `${OWNER}/exfer-py`, kind: "source" },
};

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function gh(path, attempt = 1) {
  try {
    const res = await fetch(`https://api.github.com${path}`, {
      headers: {
        accept: "application/vnd.github+json",
        "user-agent": "exfer-get-refresh",
        ...(token ? { authorization: `Bearer ${token}` } : {}),
      },
    });
    if (res.status === 404) return null;
    if (!res.ok) throw new Error(`${path} → ${res.status} ${await res.text()}`);
    return res.json();
  } catch (e) {
    if (attempt >= 4) throw e;
    await sleep(attempt * 1500); // transient network blip — back off and retry
    return gh(path, attempt + 1);
  }
}

const stripV = (t) => (t || "").replace(/^v/, "");

function classifyDesktop(assets) {
  const out = {};
  for (const a of assets) {
    const n = a.name;
    if (/\.sig$/.test(n) || n === "latest.json" || /\.app\.tar\.gz$/.test(n)) continue;
    const e = { name: n, url: a.browser_download_url };
    if (/\.dmg$/.test(n)) out.macos = e;
    else if (/setup\.exe$/.test(n) || (/\.exe$/.test(n) && !out.windows)) out.windows = e;
    else if (/\.deb$/.test(n)) out.linux_deb = e;
    else if (/\.AppImage$/.test(n)) out.linux_appimage = e;
    else if (/\.rpm$/.test(n)) out.linux_rpm = e;
  }
  return out;
}

async function buildDesktop(repo) {
  const rel = await gh(`/repos/${repo}/releases/latest`);
  if (!rel) return { version: null, repo, releaseUrl: `https://github.com/${repo}/releases`, assets: {} };
  return {
    version: stripV(rel.tag_name),
    repo,
    releaseUrl: `https://github.com/${repo}/releases/latest`,
    assets: classifyDesktop(rel.assets || []),
  };
}

async function buildSource(repo) {
  const rel = await gh(`/repos/${repo}/releases/latest`);
  return {
    version: rel ? stripV(rel.tag_name) : null,
    repo,
    releaseUrl: rel ? `https://github.com/${repo}/releases/latest` : `https://github.com/${repo}`,
    kind: "source",
  };
}

async function buildMobile() {
  // The mobile repo is private; its release workflow mirrors the apk/aab into
  // THIS repo under a `mobile-vX.Y.Z` tag. Find the newest such release.
  const rels = (await gh(`/repos/${SELF}/releases?per_page=100`)) || [];
  const mob = rels
    .filter((r) => /^mobile-v/.test(r.tag_name) && !r.draft)
    .sort((a, b) => (a.published_at < b.published_at ? 1 : -1))[0];
  const out = {
    version: mob ? stripV(mob.tag_name.replace(/^mobile-/, "")) : null,
    repo: SELF,
    releaseUrl: `https://github.com/${SELF}/releases`,
    assets: {},
    stores: { play: null, appstore: null },
  };
  if (mob) {
    const apk = (mob.assets || []).find((a) => /\.apk$/.test(a.name));
    if (apk) out.assets.android = { name: apk.name, url: apk.browser_download_url };
  }
  return out;
}

const components = {};
for (const [key, cfg] of Object.entries(PUBLIC)) {
  components[key] = cfg.kind === "desktop" ? await buildDesktop(cfg.repo) : await buildSource(cfg.repo);
}
components.mobile = await buildMobile();

// Keep a stable key order so an unchanged run produces byte-identical output.
const ordered = {
  desktop: components.desktop,
  mobile: components.mobile,
  node: components.node,
  indexer: components.indexer,
  walletd: components.walletd,
  py: components.py,
};

const fs = await import("node:fs/promises");
let prev = null;
try {
  prev = JSON.parse(await fs.readFile("releases.json", "utf8"));
} catch {
  /* first run / missing */
}

// Only bump `updated` when the actual component data changed — otherwise the
// daily cron would churn a no-op commit on every run.
const unchanged = prev && JSON.stringify(prev.components) === JSON.stringify(ordered);
const updated = unchanged ? prev.updated : new Date().toISOString();

const out = {
  _comment: "Auto-generated by .github/workflows/refresh-releases.yml — do not hand-edit.",
  updated,
  components: ordered,
};

await fs.writeFile("releases.json", JSON.stringify(out, null, 2) + "\n");
console.log(`wrote releases.json @ ${updated}${unchanged ? " (no change)" : " (updated)"}`);
for (const [k, v] of Object.entries(ordered)) console.log(`  ${k}: ${v.version || "—"}`);
